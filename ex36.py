# Rules for if-statements
# 1. Every if-statement must have an else.
# 2. If this else should never run because it doesn't make sense,
#	 then you must use a die function in the else that prints out
# 	 an error message and dies.
# 3. Never nest if-statements more than two deep
# 	 and always try to do them one deep.
# 4. Treat if-statements like paragraphs, where each if-elif-else grouping
# 	 is like a set of sentences. Put blank lines before and after.
# 5. Boolean tests should be simple. If they are complex, move their
#	 calculations to variables earlier in your function.

# Rules for loops
# 1. Use a while-loop to loop forever, and the probably means never
# 2. Use a for-loop for all other types of looping

# Tips for debugging
# 1. Don't use a debugger
# 2. Use the print command
# 3. Write small pieces of code at a time 